                        <h2>Que são Cookies</h2>
                        <p>Cookies são pequenos pedaços de texto que os sites que você visita enviam para o seu navegador. Ele permite que os sites registrem informações sobre sua visita, o que pode facilitar a revisitação dos sites e torná-los mais úteis. Tecnologias semelhantes, como identificadores exclusivos usados ​​para identificar um aplicativo ou dispositivo, pixel tags e armazenamento local, podem executar a mesma função. Os cookies e tecnologias semelhantes descritos nesta página podem ser utilizados para os fins descritos abaixo.</p>

