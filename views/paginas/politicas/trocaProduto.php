                        <h1>Política de troca e devoluções</h1>
                        
                        <p>Estes Termos e Condições da Política de Trocas e devoluções de Produtos, que tem como diretrizes e parâmetros as disposições constantes da Lei 8.078/90 (“Código de Defesa do Consumidor”) objetivam estabelecer as condições aplicáveis às trocas de produtos adquiridos, na marca MISTI dentro do São Paulo / Santo André / Brasil CNPJ Nº. 07.984.267/0001-00 , por consequência, os produtos poderão ser trocados (dentro das condições estabelecidas nesta Política).</p>
                        <p>Ao adquirir os produtos, o consumidor concorda com os termos e condições desta Política, regida pelos seguintes termos, que inclusive dispõe sobre as situações para utilização:</p>


                        <h2>Trocas por vício e/ou defeitos em produtos não duráveis</h2>
                        <p>Na hipótese de constatação pelo consumidor de vício de qualidade e/ou quantidade que torne impróprio ou inadequado para o consumo quaisquer produtos de natureza não durável, adquiridos pela marca MISTI em São Paulo / Santo André / Brasil , Misti realizará a substituição de tais produtos por outro idêntico, em perfeitas condições de consumo, desde que apresentados no momento da compra para troca, por se tratarem de produtos para consumo imediato.</p>
                        <p>Caso não haja na marca Misti outro produto semelhante de natureza não durável e/ou o consumidor optar por não aceitar a troca por outro produto idêntico, pode este a seu critério, requerer a restituição imediata.</p>


                        <h2>Trocas por vício e/ou defeitos em produtos duráveis</h2>
                        <p>Caso o vício ou defeito seja constatado em produtos duráveis (conforme definido na nota 1 acima), adquiridos pela marca Misi no São Paulo / Santo André / Brasil, o consumidor poderá informar a ocorrência, mediante a apresentação do produto diretamente em a red sociais de Misti  (Instagram: @misti.bonito_br),  no prazo de até 90 (noventa) dias da data da sua aquisição.</p>
                        <p>Após ciência da ocorrência o produto será encaminhado à Misti para análise no intuito de verificar o vício e/ou defeito apresentado para em até 30 (trinta) dias sanar o vício caso assim constatado. Ocorrendo a impossibilidade de sanar eminente vício constatado, serão apresentadas as seguintes soluções ao consumidor:</p>
                        <p class="mst-troca-produtos-listado-users">A substituição do produto por outro da mesma espécie, em perfeitas condições de uso.</p>
                        <p>A restituição imediata da quantia paga.</p>
                        <p>No caso de ser escolhida a substituição do produto por outro de mesma espécie e não houver a sua disponibilidade em website, poderá ser substituído por outro de espécie distinta ou modelo, mediante complementação ou restituição de eventual diferença de valor.</p>

                        <h2>Procedimento para troca de produto</h2>
                        <p>A troca do produto ficará condicionada à análise das condições em que foram apresentadas, bem como a não existência de mau uso por parte do consumidor. Identificada qualquer divergência, a Misti ficará isenta de adotar o procedimento para conserto ou troca do produto danificado.</p>
                        <p>Para cada uma das alterações por defeitos e/ou defeitos deverá ser apresentado o comprovante (chat do Instagram onde você fez seu pedido com a Misti) junto com o produto a ser analisado, para que posteriormente possam ser adotadas as medidas cabíveis.</p>

